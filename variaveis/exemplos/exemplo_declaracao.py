# Declaração de variáveis com diferentes tipos de dados

# Variável inteira: armazena um número sem parte decimal.
numero_inteiro = 10  # Exemplo: o número 10

# Variável float: armazena um número com parte decimal.
numero_decimal = 3.14  # Exemplo: o valor aproximado de Pi

# Variável string: armazena uma sequência de caracteres.
mensagem = "Olá, mundo!"  # Exemplo: uma mensagem de saudação

# Variável booleana: armazena um valor True ou False.
esta_ativo = True  # Exemplo: indica se um sistema ou função está ativo

# Exibição dos valores das variáveis utilizando a função print()
print("Valor da variável numero_inteiro:", numero_inteiro)
print("Valor da variável numero_decimal:", numero_decimal)
print("Valor da variável mensagem:", mensagem)
print("Valor da variável esta_ativo:", esta_ativo)

# Explicação:
# Cada variável foi declarada com um nome que descreve seu conteúdo.
# Os comentários ao lado de cada declaração ajudam a entender o tipo e a finalidade.
# A função print() é utilizada para mostrar os valores armazenados em cada variável.
